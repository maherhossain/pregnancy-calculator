	<table border="2" cellpadding="10" style="width:90%; margin: auto; font-size: 16px;line-height:1.6;border-collapse: collapse; ">
		<tr>
			<th>First Day of Last Menstrual Period (LMP)</th>
			<td>The start date of the last menstrual period, used to estimate the gestational age of the fetus. It typically occurs about two weeks before ovulation and conception.</td>
		</tr>

		<tr>
			<th>Probable Date of Conception</th>
			<td>The estimated date when fertilization likely occurred, calculated based on the menstrual cycle and ovulation timing, typically around 2 weeks after the LMP.</td>
		</tr>

		<tr>
			<th>Dating Scan</th>
			<td>A prenatal ultrasound scan performed early in pregnancy (usually between 8-14 weeks) to accurately determine the gestational age of the fetus and estimate the due date.
			</td>
		</tr>

		<tr>
			<th>NIPT Testing</th>
			<td>
				Non-Invasive Prenatal Testing, a screening method that analyzes fetal DNA circulating in the mother's blood to assess the risk of chromosomal abnormalities such as Down syndrome, Edwards syndrome, and Patau syndrome.
			</td>
		</tr>

		<tr>
			<th>Nuchal Translucency Scan</th>
			<td>
				A specialized ultrasound scan performed around 11-14 weeks of pregnancy to measure the thickness of the nuchal translucency (fluid) at the back of the fetus's neck. It's used to assess the risk of chromosomal abnormalities.
			</td>
		</tr>

		<tr>
			<th>Pre-eclampsia Screening</th>
			<td>
				A series of tests including blood pressure measurement and urine analysis to assess the risk of developing pre-eclampsia, a pregnancy complication characterized by high blood pressure and organ damage.
			</td>
		</tr>

		<tr>
			<th>Structural Scan</th>
			<td>
				An ultrasound examination performed around 18-20 weeks of pregnancy to assess the fetal anatomy and detect any structural abnormalities.
			</td>
		</tr>

		<tr>
			<th>Morphology Scan</th>
			<td>
				Another term for the structural scan, focusing on detailed assessment of fetal morphology or structure to ensure normal development.
			</td>
		</tr>

		<tr>
			<th>Estimated Due Date (EDD)</th>
			<td>
				The anticipated date when the pregnancy is expected to reach full term, calculated based on the LMP, ultrasound findings, and other factors.
			</td>
		</tr>

		<tr>
			<th>Growth Scan</th>
			<td>
				A series of ultrasound scans performed throughout pregnancy to monitor fetal growth and development, ensuring the baby is growing at a healthy rate. These scans are particularly important in cases of high-risk pregnancies or concerns about fetal growth restriction.
			</td>
		</tr>
		
